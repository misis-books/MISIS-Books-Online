</div>
<p class="dev_p">
    Доступ к API может быть получен из автономной программы на компьютере или мобильном устройстве пользователя.
    <br><br>
    Взаимодействие с API из Standalone/Mobile-приложений происходит по <a href="/dev/api_requests" class="dev_link">стандартной схеме</a>. API позволяет реализовать в автономном приложении практически все функции сайта.
</p>
